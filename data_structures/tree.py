class TreeNode:
    def __init__(self, val=None, left=None, right=None):
        self.val = val
        self.left = left
        self.right = right

class Tree:
    def __init__(self, root: TreeNode = None):
        self.root = root

class BinarySearchTree:
    def __init__(self, root: TreeNode = None):
        self.root = root
